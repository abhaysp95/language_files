/*********
this keyword
*********/

/**
-> The this object reference is a local variable in instance member methds referring the caller object.
-> this keyword is used as a reference to the current object which is an instance of the current class.
-> The this reference to the current object is useful in situtations where a local variable hides, or shadows, a field with the same name.
-> If a method needs to pass the current object to another method, it can do using the this reference.
-> Note that the this reference cannot be used in static context, as static code is not executed in the context of any object.
-> Every instance member function has 'this' in itself and gets destroyed when function ends and constructor also has 'this'.*/
